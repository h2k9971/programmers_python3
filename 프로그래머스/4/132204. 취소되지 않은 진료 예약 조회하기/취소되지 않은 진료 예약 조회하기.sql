SELECT
    a.APNT_NO,
    p.PT_NAME,
    a.PT_NO,
    a.MCDP_CD,
    d.DR_NAME,
    a.APNT_YMD
FROM APPOINTMENT AS a
JOIN PATIENT    AS p ON p.PT_NO = a.PT_NO
JOIN DOCTOR     AS d ON d.DR_ID = a.MDDR_ID
WHERE a.MCDP_CD = 'CS'
AND a.APNT_CNCL_YN = 'N'
AND DATE(a.APNT_YMD) = '2022-04-13'
ORDER BY a.APNT_YMD